package demanadthree.iftandformsswitch;

/**
 * 更多条件测试
 */
public class MoreCodItIonTest {

  /**
   * 多条件语句转换
   */
  public void test1(int code) {
    switch (code) {
      case 1:
      case 100:
        System.out.println("x");
        break;
      case 2:
      case 101:
        System.out.println("x");
        break;
      case 3:
      case 103:
        System.out.println("x");
        break;
      case 4:
      case 104:
        System.out.println("x");
        break;
      case 5:
      case 105:
        System.out.println("x");
        break;
      default:
    }
  }
}
